    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String studentrollnumber = SRollnumber.getText();
        DefaultTableModel model = (DefaultTableModel)Table.getModel();
        if(studentrollnumber.isEmpty()){
            JOptionPane.showMessageDialog(this , "Please Enter RollNumber");
        }
        try{
            model.setRowCount(0);
           //Load Driver
           Class.forName("com.mysql.cj.jdbc.Driver");
           //Create Connectio 
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hello","root","Anj@2025"); 
           String query = "SELECT * FROM student WHERE Rollnumber = ?";
           PreparedStatement pstmt = con.prepareStatement(query);
           pstmt.setString(1, studentrollnumber);
           pstmt.executeQuery();
           ResultSet rs = pstmt.executeQuery(); 
           
           boolean hasResult = false;
           while(rs.next()){
               hasResult = true;
              int sRollnumber = rs.getInt("Rollnumber");
              String studentRollnumber = Integer.toString(sRollnumber);
               String studentname = rs.getString("name");
               String studentclass = rs.getString("class");
               String row[] ={studentRollnumber,studentname ,studentclass};
               model.addRow(row);
               
           }
           if(!hasResult){
               JOptionPane.showMessageDialog(this, "Record Not Found");
           }
           
        }catch(Exception e){
            JOptionPane.showConfirmDialog(this, e);
        }
            
       
    }//GEN-LAST:event_jButton1ActionPerformed
    
