        // TODO add your handling code here:
        try{
           //Load Driver
           Class.forName("com.mysql.cj.jdbc.Driver");
           //Create Connectio 
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hello","root","Anj@2025");
           //Create Query
           String query = "SELECT * FROM student";
           PreparedStatement patmt = con.prepareStatement(query);
           //Execute Query
           ResultSet rs = patmt.executeQuery();
           DefaultTableModel model = (DefaultTableModel)Table.getModel();
           while(rs.next()){
               int sRollnumber = rs.getInt("Rollnumber");
               String studentRollnumber = Integer.toString(sRollnumber);
               String studentName = rs.getString("name");
               String className = rs.getString("class");
               String row[]={studentRollnumber , studentName , className};
               model.addRow(row);
               }
           con.close();
               
        }catch(ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(this, e);
        }
